void main() {
  int a = 10;
  int b = 20;
  
  // Arithmetic operators
  print(a + b); // 30
  print(a - b); // -10
  print(a * b); // 200
  print(b / a); // 2.0
  
  // Relational operators
  print(a == b); // false
  print(a != b); // true
  print(a > b); // false
  
  // Logical operators
  bool x = true;
  bool y = false;
  
  print(x && y); // false
  print(x || y); // true
  print(!x); // false
}
